class V_CHECK extends V {
    constructor(){
        super();
        this.valid = [];
    };

    formSubmit(formID) {
        document.getElementById(formID).submit();
    };

    checkNode(node){
        var node = document.getElementById(n.id);
        const pattern = n.pattern;

        if (pattern.test(node)) this.valid.push(node)
    }

    checkForm(formID, params) {
        params.inputs.forEach(node => {
            try {
                console.log(node);
                if (node.id && node.pattern) this.checkNode({
                    id: node.id,
                    pattern: node.pattern
                })
            } catch (error) {
                throw new Error(error)
            }
        });
    }
}

